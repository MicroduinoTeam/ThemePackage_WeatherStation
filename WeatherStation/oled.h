//显示函数
void volcd(float temp, float humi, float light, float pm25) {
  u8g.firstPage();
  do {
    u8g.setDefaultForegroundColor();

    u8g.drawXBMP( 4, 1, 15, 32, bmp_tem);  //温度图标
    u8g.drawXBMP( 70, 2, 24, 30, bmp_hum); //湿度图标

    u8g.setFont(setFont_M);              //设置字体为大
    u8g.setPrintPos(20, 16);              //设置文字开始坐标
    u8g.print("`C ");
    u8g.setFont(setFont_L);                //设置字体为大
    u8g.setPrintPos(20, 32);              //设置文字开始坐标
    u8g.print(temp , 1);                  //显示温度

    u8g.setFont(setFont_M);               //设置字体为大
    u8g.setPrintPos(100, 16);             //设置文字开始坐标
    u8g.print("%");
    u8g.setFont(setFont_L);              //设置字体为大
    u8g.setPrintPos(100, 32);            //设置文字开始坐标
    u8g.print(humi , 0);                //湿度

    u8g.setFont(setFont_M);
    u8g.setPrintPos(0, 48);
    u8g.print("LUX:");
    u8g.setFont(setFont_S);
    u8g.setPrintPos(36, 48);          //设置文字开始坐标
    u8g.print(light , 1);
    u8g.print("lux");

    u8g.setFont(setFont_M);
    u8g.setPrintPos(0, 62);
    u8g.print("PM:");
    u8g.setFont(setFont_S);
    u8g.setPrintPos(34, 62);          //设置文字开始坐标
    u8g.print(pm25 , 1);
    u8g.print("ug/m3");
  }
  while ( u8g.nextPage() );
}
